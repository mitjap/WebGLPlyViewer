   var gl;
    function initGL(canvas) {
		try {
			resizeGL(false);
            gl = canvas.getContext("webgl");
            gl.viewportWidth = canvas.width;
            gl.viewportHeight = canvas.height;
        } catch (e) {
        }
        if (!gl) {
            alert("Could not initialise WebGL, sorry :-(");
        }
    }


    function getShader(gl, id) {
        var shaderScript = document.getElementById(id);
        if (!shaderScript) {
            return null;
        }

        var str = shaderScript.textContent;	

        var shaderTypes = {
			'x-shader/x-fragment': gl.FRAGMENT_SHADER,
			'x-shader/x-vertex': gl.VERTEX_SHADER
		};

		shader = gl.createShader(shaderTypes[shaderScript.type]);
		
		gl.shaderSource(shader, str);
        gl.compileShader(shader);

        if (!gl.getShaderParameter(shader, gl.COMPILE_STATUS)) {
            throw gl.getShaderInfoLog(shader);
        }

        return shader;
    }


    var shaderProgram;

    function initShaders() {
        var fragmentShader = getShader(gl, "fshader");
        var vertexShader = getShader(gl, "vshader");

        shaderProgram = gl.createProgram();
        gl.attachShader(shaderProgram, vertexShader);
        gl.attachShader(shaderProgram, fragmentShader);
        gl.linkProgram(shaderProgram);

        if (!gl.getProgramParameter(shaderProgram, gl.LINK_STATUS)) {
            alert("Could not initialise shaders");
        }

        gl.useProgram(shaderProgram);

        shaderProgram.vertexPositionAttribute = gl.getAttribLocation(shaderProgram, "aVertexPosition");
        gl.enableVertexAttribArray(shaderProgram.vertexPositionAttribute);
		
		shaderProgram.vertexColorAttribute = gl.getAttribLocation(shaderProgram, "aVertexColor");
        gl.enableVertexAttribArray(shaderProgram.vertexColorAttribute);

        shaderProgram.projectionMatUniform = gl.getUniformLocation(shaderProgram, "projection_mat");
        shaderProgram.modelMatUniform = gl.getUniformLocation(shaderProgram, "model_mat");
        shaderProgram.viewMatUniform = gl.getUniformLocation(shaderProgram, "view_mat");
    }


